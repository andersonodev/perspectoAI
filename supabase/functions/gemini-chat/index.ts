
import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2.7.1';

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { 
      message, 
      assistantId, 
      sessionId, 
      conversationHistory = [],
      assistantSettings = {},
      learningProfile = null,
      isCommand = false,
      isPracticeMode = false,
      isActivityGeneration = false 
    } = await req.json();

    console.log('Processing request:', { message, assistantId, sessionId, assistantSettings });

    const supabaseUrl = Deno.env.get('SUPABASE_URL')!;
    const supabaseKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!;
    const geminiApiKey = Deno.env.get('GEMINI_API_KEY');

    if (!geminiApiKey) {
      throw new Error('GEMINI_API_KEY not configured');
    }

    const supabase = createClient(supabaseUrl, supabaseKey);

    // Get assistant details
    const { data: assistant, error: assistantError } = await supabase
      .from('ai_assistants')
      .select('*')
      .eq('id', assistantId)
      .single();

    if (assistantError || !assistant) {
      throw new Error('Assistant not found');
    }

    // Get assistant knowledge
    const { data: knowledge } = await supabase
      .from('assistant_knowledge')
      .select('*')
      .eq('assistant_id', assistantId);

    // Build context from knowledge base
    const knowledgeContext = knowledge?.map(k => `${k.title}: ${k.content}`).join('\n\n') || '';

    // Get AI control settings
    const creativityLevel = assistantSettings.creativityLevel || 50;
    const citationMode = assistantSettings.citationMode || false;
    const transparencyMode = assistantSettings.transparencyMode || false;

    // Build adaptive personality prompt
    let personalityPrompt = `Voc√™ √© ${assistant.name}, um assistente de IA especializado em ${assistant.subject}.`;
    
    // Apply creativity level settings
    if (creativityLevel <= 30) {
      personalityPrompt += `
      MODO GUARDI√ÉO DO CONTE√öDO:
      - Atenha-se ESTRITAMENTE ao material fornecido na base de conhecimento
      - Se a informa√ß√£o n√£o estiver no material, diga claramente: "Com base no material fornecido, essa informa√ß√£o n√£o est√° dispon√≠vel"
      - Nunca invente ou extrapol–µ informa√ß√µes
      - Cite sempre a fonte espec√≠fica do material`;
    } else if (creativityLevel > 70) {
      personalityPrompt += `
      MODO PARCEIRO DE IDEIAS:
      - Use o material fornecido como base, mas pode criar analogias criativas
      - Conecte ideias usando conhecimento geral para enriquecer o aprendizado
      - Crie exemplos novos e met√°foras que ajudem na compreens√£o
      - Seja criativo mas sempre mantenha a precis√£o acad√™mica`;
    } else {
      personalityPrompt += `
      MODO EQUILIBRADO:
      - Use principalmente o material fornecido
      - Pode criar analogias simples e exemplos baseados no conte√∫do
      - Mantenha um equil√≠brio entre precis√£o e criatividade`;
    }

    // Apply personality traits
    if (assistant.personality === 'friendly') {
      personalityPrompt += ' Seja caloroso, encorajador e use linguagem acess√≠vel.';
    } else if (assistant.personality === 'formal') {
      personalityPrompt += ' Mantenha um tom profissional e direto.';
    } else if (assistant.personality === 'socratic') {
      personalityPrompt += ' Use o m√©todo socr√°tico, fazendo perguntas que guiem o aluno ao entendimento.';
    } else if (assistant.personality === 'creative') {
      personalityPrompt += ' Use analogias criativas, met√°foras e exemplos divertidos.';
    }

    // Add citation requirements
    if (citationMode) {
      personalityPrompt += `
      CITA√á√ÉO OBRIGAT√ìRIA:
      - Para cada informa√ß√£o espec√≠fica, adicione uma cita√ß√£o entre par√™nteses
      - Formato: (Fonte: [nome do documento], p√°gina/se√ß√£o [X])
      - Se usar m√∫ltiplas fontes, liste todas
      - Torne as cita√ß√µes discretas mas vis√≠veis`;
    }

    // Add transparency mode
    if (transparencyMode) {
      personalityPrompt += `
      MODO TRANSPAR√äNCIA:
      - Ao final de cada resposta complexa, explique brevemente seu racioc√≠nio
      - Use uma se√ß√£o "ü§î Como cheguei a esta resposta:" 
      - Seja claro sobre quais fontes usou e como conectou as informa√ß√µes`;
    }

    // Adaptive learning enhancements
    if (learningProfile) {
      if (learningProfile.preferences?.includes('examples')) {
        personalityPrompt += ' O aluno aprende melhor com exemplos pr√°ticos.';
      }
      if (learningProfile.preferences?.includes('analogies')) {
        personalityPrompt += ' Use analogias e met√°foras para explicar conceitos.';
      }
      personalityPrompt += ` Ajuste seu ritmo para: ${learningProfile.pace}.`;
    }

    // Special modes
    if (isPracticeMode) {
      personalityPrompt += `
        MODO PR√ÅTICA ATIVADO: Gere exerc√≠cios pr√°ticos com:
        1. Uma pergunta desafiadora mas adequada ao n√≠vel
        2. Dicas progressivas se necess√°rio
        3. Explica√ß√£o detalhada da solu√ß√£o
        4. Contexto de aplica√ß√£o pr√°tica
        Formate em markdown para melhor visualiza√ß√£o.`;
    }

    if (isActivityGeneration) {
      personalityPrompt += `
        GERA√á√ÉO DE ATIVIDADE: Crie uma atividade educacional completa:
        1. Objetivo de aprendizagem claro
        2. Instru√ß√µes passo a passo
        3. Exerc√≠cios variados (m√∫ltipla escolha, dissertativa, pr√°tica)
        4. Rubrica de avalia√ß√£o
        5. Recursos adicionais recomendados
        Formate em markdown para f√°cil leitura.`;
    }

    if (isCommand) {
      personalityPrompt += ' Esta √© uma solicita√ß√£o de comando especial (resumo, an√°lise, etc). Responda de forma estruturada e completa.';
    }

    // Add behavioral instructions
    if (assistant.guardrails?.instructions) {
      personalityPrompt += `\n\nInstru√ß√µes espec√≠ficas: ${assistant.guardrails.instructions}`;
    }

    // Prepare conversation history
    const historyText = conversationHistory
      .map((msg: any) => `${msg.role === 'user' ? 'Aluno' : 'Assistente'}: ${msg.content}`)
      .join('\n');

    const systemPrompt = `${personalityPrompt}

Base de Conhecimento:
${knowledgeContext}

Instru√ß√µes importantes:
- Use a base de conhecimento para responder perguntas
- Se n√£o souber algo, diga claramente "N√£o tenho essa informa√ß√£o no meu material" e sugira onde o aluno pode buscar
- Sempre formate sua resposta usando markdown quando apropriado (listas, negrito, etc.)
- Seja preciso e educativo
- Incentive o pensamento cr√≠tico
- Se detectar uma d√∫vida comum, sugira t√≥picos relacionados

Hist√≥rico da conversa:
${historyText}`;

    // Call Gemini API
    const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/gemini-pro:generateContent?key=${geminiApiKey}`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        contents: [
          {
            parts: [
              {
                text: `${systemPrompt}\n\nPergunta do aluno: ${message}`
              }
            ]
          }
        ],
        generationConfig: {
          temperature: creativityLevel > 50 ? 0.8 : 0.4, // Adjust temperature based on creativity level
          topK: 40,
          topP: 0.95,
          maxOutputTokens: 2048,
        }
      })
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('Gemini API error:', errorText);
      throw new Error(`Gemini API error: ${response.status}`);
    }

    const data = await response.json();
    console.log('Gemini response:', data);

    if (!data.candidates || data.candidates.length === 0) {
      throw new Error('No response from Gemini API');
    }

    const aiResponse = data.candidates[0].content.parts[0].text;

    // Extract citations if citation mode is enabled
    let citations: string[] = [];
    let reasoning = '';

    if (citationMode) {
      const citationRegex = /\(Fonte: ([^)]+)\)/g;
      let match;
      while ((match = citationRegex.exec(aiResponse)) !== null) {
        citations.push(match[1]);
      }
    }

    // Extract reasoning if transparency mode is enabled
    if (transparencyMode) {
      const reasoningMatch = aiResponse.match(/ü§î Como cheguei a esta resposta:\s*(.+?)(?=\n\n|\n$|$)/s);
      if (reasoningMatch) {
        reasoning = reasoningMatch[1].trim();
      }
    }

    // Generate intelligent suggestions based on the response and context
    let suggestions: string[] = [];
    
    if (!isCommand && !isActivityGeneration) {
      // Basic suggestions
      suggestions = [
        'Pode dar um exemplo pr√°tico?',
        'Como isso se aplica no dia a dia?',
        'Tem algum exerc√≠cio sobre isso?'
      ];

      // Context-aware suggestions
      if (aiResponse.includes('conceito') || aiResponse.includes('defini√ß√£o')) {
        suggestions.push('Pode explicar de forma mais simples?');
      }
      
      if (aiResponse.includes('f√≥rmula') || aiResponse.includes('c√°lculo')) {
        suggestions.push('Vamos praticar com um problema?');
      }

      if (assistant.subject.toLowerCase().includes('hist√≥ria')) {
        suggestions.push('Qual foi o contexto hist√≥rico?');
      } else if (assistant.subject.toLowerCase().includes('matem√°tica')) {
        suggestions.push('Vamos resolver um exerc√≠cio?');
      } else if (assistant.subject.toLowerCase().includes('ci√™ncia')) {
        suggestions.push('Como isso funciona na pr√°tica?');
      }
    }

    // Save conversation to database for analytics
    if (!isCommand && !isActivityGeneration) {
      try {
        await supabase
          .from('student_conversations')
          .insert({
            assistant_id: assistantId,
            student_session_id: sessionId,
            message: message,
            response: aiResponse,
            sources: knowledge?.map(k => k.title) || []
          });
      } catch (dbError) {
        console.error('Error saving conversation:', dbError);
        // Don't fail the request if we can't save to DB
      }
    }

    return new Response(
      JSON.stringify({ 
        response: aiResponse,
        suggestions: suggestions.slice(0, 3),
        citations: citations,
        reasoning: reasoning
      }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );

  } catch (error) {
    console.error('Error in gemini-chat function:', error);
    return new Response(
      JSON.stringify({ 
        error: 'Erro interno do servidor',
        details: error.message 
      }),
      { 
        status: 500,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );
  }
});
